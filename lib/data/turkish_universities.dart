  static List<String> searchUniversities(String query) {
    if (query.isEmpty) return universities.take(20).toList();

    final lowerQuery = query.toLowerCase();
    return universities
        .where((uni) => uni.toLowerCase().contains(lowerQuery))
        .take(10)
        .toList();
  }

